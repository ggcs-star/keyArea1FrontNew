const createInfiniteVideos = () => {
  const repeatCount = 10; // Number of times to repeat the array
  let infiniteVideos = [...reelVideos];

  // Then add duplicates with unique identifiers
  for (let i = 1; i < repeatCount; i++) {
    const duplicatedReels = reelVideos.map(reel => ({
      ...reel,
      id: `${reel.id}-${i}`,
      linkId: `${reel.linkId}-${i}`,
      // Deep clone other properties to ensure they're unique
      projectFeatures: [...reel.projectFeatures],
      suitableFor: [...reel.suitableFor],
      // Add some variation to make each duplicate unique
      likes: Math.floor(reel.likes * (0.8 + Math.random() * 0.4)), // Random variation of ±20%
      comments: Math.floor(reel.comments * (0.8 + Math.random() * 0.4)),
      views: Math.floor(reel.views * (0.8 + Math.random() * 0.4))
    }));
    infiniteVideos = [...infiniteVideos, ...duplicatedReels];
  }
  return infiniteVideos;
};

export const infiniteVideos = createInfiniteVideos();









